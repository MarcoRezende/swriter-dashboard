import Head from "next/head";
import { useEffect, useState } from "react";

import { Box } from "@chakra-ui/layout";
import { RequestQueryBuilder } from "@nestjsx/crud-request";

import { Table } from "../../components/common/Table";
import { Hint } from "../../entities/Hint";
import hintsModel from "../../models/hints.model";
import { getManyBase } from "../../services/common";
import { hintResource } from "../../services/hint";
import { formatDate } from "../../utils/date";

import type { NextPage } from "next";
const Home: NextPage = () => {
  const [hints, setHints] = useState<Hint[]>([]);

  useEffect(() => {
    const fetchData = async () => {
      const requestQuery = RequestQueryBuilder.create().sortBy({
        field: "createdDate",
        order: "DESC",
      }).queryObject;

      const hints = await getManyBase<Hint>({
        resource: hintResource,
        requestQuery,
      });

      setHints(hints);
    };

    fetchData();
  }, []);

  const columns = ["author", "book", "tip", "createdDate", "updatedDate"];

  return (
    <Box>
      <Head>
        <title>Sentenças</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Box role="main" p={"2rem"}>
        <Table
          model={hintsModel}
          title="Sentenças"
          columns={columns}
          uploadEndpoint={`${hintResource}/importCsv`}
        />
      </Box>
    </Box>
  );
};

export default Home;
